				<h2>Breaking the Gridlock</h2>
				
				<p>As a Colonel in the U.S. Marine Corps, I have dealt with my share of gridlock and I will use that experience to help break the political quagmire that has enveloped Washington.  It is critical that we work together to fix this country's problems.  I will not compromise my principles, but if we are going to fix this country's major problems we need to work together as a team.</p>
				
				<p>During my time overseas, my success was not only dictated by my ability to lead, fight, and win, but also my ability to listen to a diverse array of tribes and political factions, and comprehend their problems so as to find resolution. That required me to bring people together of diverse opinions, to achieve real solutions.  That is one of the things I take a great deal of pride in, both in and out of uniform.</p>
				
				<p>It is also crucial that people come before politics.  When people come ahead of politics, things like tax reform, social security reform, and immigration reform will finally start to get accomplished.</p>
				
				<p>Finally, breaking the gridlock requires selfless service.  My service in Washington, DC would be marked by direct, pragmatic, and honest engagement with my constituents and colleagues, regardless of party label, to come together to achieve meaningful solutions for our district and our country.</p>
				
				<h2>Real Health Care Reform</h2>

				<p>The Affordable Care Act (ACA) has proven to be the biggest job killer we have faced in modern history and it must go.  I believe that we must reform our health care system to reduce costs and enhance the well being of all Americans.  But such a plan does not require a government mandate.  Instead, we should open the market to competition, by allowing individuals to purchase coverage across state lines and think critically about the need for things like tort reform to mitigate out of control malpractice litigation.  The intent of the ACA may have been noble, but the empirical evidence just does not stack up.  Once again, by opening markets and combating crony capitalism, we can solve many of the challenges facing America's health care system today and ensure it remains the best health care available in the world.</p>
				
				<h2>Getting America Back on Track</h2>

				<p>America cannot tax, borrow and spend our way to prosperity.  To get America back on track we need to simplify and reform the tax code.  The tax code is too long, too complex and rates are too high.  By simplifying the tax code we will create more jobs, open the world markets to American goods and create a fair and level playing field for all American businesses.</p>
				
				<p>We also must pass a balanced budget amendment.  49 of 50 states require a balanced budget and it is time that the federal government required a balanced budget as well.  A balanced budget would bring an end to out of control government spending and is the best way to bring fiscal discipline back to the United States.</p>
				
				<p>Finally, and most importantly, we need a return to the fundamental principles that made this country great.  When I read the Constitution, I feel it. Its words have special, enduring meaning, from which our Founding Fathers communicated a brilliant method of governance.</p>
				
				<p>As your Representative, I will again take an oath of office to uphold our founding document. And I promise you that I will live out that oath to the letter. I will judge my votes against the standard of our Constitution and the will of 'We The People.' There will be no pandering to special interest groups when I make decisions. This is an important time for our nation. It is a time to solve problems, work together and not abdicate our core, Constitutional principles.</p>
				
				<h2>Peace Through Strength</h2>	

				<p>The United States should remain the lead for the free world providing an example for other countries to emulate.  It should not become involved in other countries unless there are national interests being threatened and interaction is necessary to ensure security and the quality of life for Americans.  The interest of our allies would also be considered.  The United States should try to support countries suffering humanitarian crisis and disasters but not at the expense of its own well being.  The focus right now needs to be at home, everything else is secondary until we become more capable and stronger economically with less debt and lower deficits. </p>
				
				<p>The United States, if it does decide to get involved, should only do so after careful evaluation of the conflict at hand and after identifying an honest strategy which will deliver the desired results. The United States can never allow political correctness to prevent an honest assessment of the conflict at hand which would result in a flawed strategy and unsuccessful campaign.</p>
				
